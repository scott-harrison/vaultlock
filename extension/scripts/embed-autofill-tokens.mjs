#!/usr/bin/env node
/**
 * Embeds @vaultlock/ui autofill-tokens.css into the content-script bundle
 * so shadow DOM UI can use design tokens without a per-tab stylesheet.
 */

import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const tokensPath = join(root, "../ui/src/styles/autofill-tokens.css");
const outputPath = join(root, "src/contents/lib/autofillTokensCss.ts");

const css = readFileSync(tokensPath, "utf8");
const source = `// Generated by scripts/embed-autofill-tokens.mjs — do not edit.\nexport const AUTOFILL_TOKENS_CSS = ${JSON.stringify(css)};\n`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, source);
console.log("[VaultLock] Embedded autofill tokens for content scripts");
